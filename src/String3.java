public class String3 {
    public static void main (String [] args) {
        int x =5; // Примитивный тип данных
        // в этом уроке - ссылочные типы данных
        String s = "Hello World";  // String - это класс, поэтому с большой буквы. "Hello World" - это объект
        // Класс - это схема, чертеж. А объект - это машина сделнная по этому чертежу.
        String space = " ";
        String name = "Bob";
        System.out.println(s+space+name);
        System.out.println("Hi"+" "+"BoDy!");
        System.out.println("my number " + x); //sout
    }
}
