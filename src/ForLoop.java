public class ForLoop {
    public static void main(String[] args) {
        for (int i = 10; i >=0; i = i-5) {   // в первом блоке - переменная, которая будет меняться;
            // во втором блоке - условие до которого будет выполняться цикл;
            // третий - действие шага
            System.out.println("Hii " + i);

        }

    }
}
